# Relaciones -> simbolos para comparar dos valores = Boolean
v1 = 10
v2 = 18
# Relacionales - mayor, menor, mayorIgual, menorIgual, igual, diferente
mayor = v1 > v2
menor = v1 < v2
mayor_igual = v1 >= v2
menor_igual = v1 <= v2
igual = v1 == v2
diferente = v1 != v2

print(mayor)
print(menor)
print(mayor_igual)
print(menor_igual)
print(igual)
print(diferente)

# Logicos -> AND, OR y NOT
# AND -> todo verdadero -> da un booleano TRUE
# OR -> alguno Verdado -> para que sea verdadero
# NOT -> lo contrario

print(not True)
print(True and True)
print(True or False)
